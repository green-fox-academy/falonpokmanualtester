package feladatok;

public class Bmi {
    public static void main(String[] args) {
        double massInKg = 81.2;
        double heightInM = 1.78;
        System.out.println("BMI: " + massInKg / (heightInM * heightInM));
        // Számíttasd és írasd ki a testtömeg-indexet (BMI)
        // a változókat használva
    }
}
